def sum(numA, numB):
    return numA + numB
def multiply(numA, numB):
    return numA * numB
def substract(numA, numB):
    return numA - numB
def multiply_three_numbers(numA, numB, numC):
    return numA * numB * numC
def add_and_multiply(numA, numB, numC):
    return (numA + numB) * numC

print(sum(3,2))
print(multiply(3,2))
print(substract(3,2))
print(multiply_three_numbers(3,2,4))
print(add_and_multiply(3,2,4))